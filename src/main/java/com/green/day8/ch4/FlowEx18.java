package com.green.day8.ch4;

public class FlowEx18 {
    public static void main(String[] args){
        for(int i=1;i<=9;i++){
            for(int j=1;j<=9;j++){
                System.out.println(i+"X"+j+"="+i*j);
            }System.out.println();
        }//이중 포문=시계 //빠르게 도는 게 분=안 쪽 for문
    }
}
